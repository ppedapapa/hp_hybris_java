DROP TABLE USER;

CREATE TABLE
    USER
    (
        SHAKLEE_ID VARCHAR(20) NOT NULL,
        CONTACT_ID VARCHAR(20) NOT NULL,
        FIRST_NAME VARCHAR(4000),
        LAST_NAME VARCHAR(4000),
        EMAIL VARCHAR(128),
        CREATED DATETIME NOT NULL,
        UPLINE_SHAKLEE_ID VARCHAR(20),
        SPONSOR_SHAKLEE_ID VARCHAR(20),
        ACCOUNT_TYPE VARCHAR(20),
        COUNTRY VARCHAR(4),
        PREFERRED_LANGUAGE VARCHAR(6),
        PRIMARY KEY (CONTACT_ID)
    );
    
